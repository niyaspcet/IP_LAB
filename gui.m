function popupmenu1_Callback(hObject, eventdata, handles)
% hObject    handle to popupmenu1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

% Hints: contents = cellstr(get(hObject,'String')) returns popupmenu1 contents as cell array
%        contents{get(hObject,'Value')} returns selected item from popupmenu1
% Determine the selected data set. 
str = get(hObject, 'String'); 
val = get(hObject,'Value'); % Set current data to the selected data set. 
switch str{val}; 
case 'Gabor Filter' % User selects peaks.  
clc;
clear all;
close all;

%Reading the image
I=imread('C:\Users\Visakh\Desktop\DIP_Case_Study\Database\107_2.tif');
[m n]=size(I);
s=zeros(m,n);
w=zeros(m,n);

%Applying Gabor Filter at different angles from 0 to 179.5 degrees by the
%increment of 0.5
for i=0:0.5:179.5
       [m p] = imgaborfilt(I,2,i);
       s=s+m;
       w=w+p;
end
s=s/361;
%Taking Averege
%Converting range of values to 0-255 range
mi=min(min(s));
ma=max(max(s));
s=s-mi;
k=255/ma;
s=s*k;
s=uint8(255-s);
avg=200;

%s(s<avg)=0;
%s(s>=avg)=255;

%Plot
figure
subplot(122);
imshow(uint8(s));
title('Enhanced Image');

subplot(121);
imshow(uint8(I));
title('Original Image');

case 'Curved Gabor Filter' % User selects membrane.   
 
end % Save the handles 
